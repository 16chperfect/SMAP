package com.example.smap_google;

public class gesipan {
    private  int iv_pro;
    private  String tv_name;

    public gesipan(int iv_pro, String tv_name) {
        this.iv_pro = iv_pro;
        this.tv_name = tv_name;
    }

    public int getIv_pro() {
        return iv_pro;
    }

    public void setIv_pro(int iv_pro) {
        this.iv_pro = iv_pro;
    }

    public String getTv_name() {
        return tv_name;
    }

    public void setTv_name(String tv_name) {
        this.tv_name = tv_name;
    }
}
